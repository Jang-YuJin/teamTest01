package projectDTO;

public class ProductDTO {

	private String p_code;
	private String p_name;
	private int p_price;
	private String p_representative;
	private String p_details;
	
	public String getP_code() {
		return p_code;
	}
	public void setP_code(String p_code) {
		this.p_code = p_code;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public int getP_price() {
		return p_price;
	}
	public void setP_price(int p_price) {
		this.p_price = p_price;
	}
	public String getP_representative() {
		return p_representative;
	}
	public void setP_representative(String p_representative) {
		this.p_representative = p_representative;
	}
	public String getP_details() {
		return p_details;
	}
	public void setP_details(String p_details) {
		this.p_details = p_details;
	}
	
}
